DROP TABLE inventory_inv CASCADE CONSTRAINTS; 
DROP TABLE product_inv CASCADE CONSTRAINTS; 
DROP TABLE warehouse_inv CASCADE CONSTRAINTS; 
DROP TABLE department_inv CASCADE CONSTRAINTS; 
DROP TABLE vendor_inv CASCADE CONSTRAINTS; 
DROP TABLE customer_inv CASCADE CONSTRAINTS; 
 
 
CREATE TABLE customer_inv (   customer_id       NUMBER(20)      NOT NULL,   first_name        VARCHAR2(50)    NOT NULL,   last_name         VARCHAR2(50)    NOT NULL,   birthdate         DATE            NOT NULL,   email             VARCHAR2(50)    UNIQUE      NOT NULL,   prime_status      VARCHAR2(1)     DEFAULT 'N' NOT NULL,   phone_number      VARCHAR2(50),   member_start_date DATE, 
  PRIMARY KEY (customer_id) 
); 
 
CREATE TABLE vendor_inv (   vendor_id             NUMBER(20)      NOT NULL,   vendor_name           VARCHAR2(50)    NOT NULL,   vendor_phone_number   VARCHAR2(50)    NOT NULL,   vendor_contact        VARCHAR2(50)    NOT NULL,   vendor_email          VARCHAR2(50)    NOT NULL,   vendor_address        VARCHAR2(50)    NOT NULL,   vendor_city           VARCHAR2(50)    NOT NULL,   vendor_region         VARCHAR2(50)    NOT NULL,   vendor_zip            VARCHAR2(5)     NOT NULL, 
  PRIMARY KEY (vendor_id) 
); 
 
CREATE TABLE department_inv (   department_id     NUMBER(20)      NOT NULL,   department_name   VARCHAR2(50), 
  PRIMARY KEY (department_id) 
); 
 
CREATE TABLE warehouse_inv (   warehouse_id         NUMBER(20)      NOT NULL,   warehouse_address    VARCHAR2(50)    NOT NULL,   warehouse_city       VARCHAR2(50)    NOT NULL,   warehouse_region     VARCHAR2(50)    NOT NULL,   warehouse_zip        VARCHAR2(5)     NOT NULL, 
  PRIMARY KEY (warehouse_id) 
); 
 
CREATE TABLE product_inv (   product_id        NUMBER(20)      NOT NULL,   department_id     NUMBER(20)      NOT NULL,   vendor_id         NUMBER(20)      NOT NULL,   product_name      VARCHAR2(50)    NOT NULL,   unit_price        NUMBER(10)      NOT NULL,   prime_discount    NUMBER(7,2), 
  PRIMARY KEY (product_id), 
  CONSTRAINT FK_product_department_id FOREIGN KEY (department_id) REFERENCES department_inv(department_id), 
  CONSTRAINT FK_product_vendor_id FOREIGN KEY (vendor_id) REFERENCES vendor_inv(vendor_id) 
); 
 
CREATE TABLE inventory_inv (   inventory_id          NUMBER(20)      NOT NULL,   warehouse_id          NUMBER(20)      NOT NULL,   product_id            NUMBER(20)      NOT NULL,   quantity_on_hand      NUMBER(10)      NOT NULL,   quantity_available    NUMBER(20)      NOT NULL, 
  PRIMARY KEY (inventory_id), 
  CONSTRAINT FK_inventory_product_id FOREIGN KEY (product_id) REFERENCES product_inv(product_id), 
  CONSTRAINT FK_inventory_warehouse_id FOREIGN KEY (warehouse_id) REFERENCES warehouse_inv(warehouse_id) 
); 
 
CREATE TABLE orders_inv (   order_id      NUMBER(20)      NOT NULL,   customer_id   NUMBER(20)      NOT NULL,   order_date    DATE            NOT NULL, 
  PRIMARY KEY (order_id), 
  CONSTRAINT FK_orders_customer_id FOREIGN KEY (customer_id) REFERENCES customer_inv(customer_id) 
); 
 
CREATE TABLE order_details_inv (   order_details_id  NUMBER(20)      NOT NULL,   order_id          NUMBER(20)      NOT NULL,   product_id        NUMBER(20)      NOT NULL,   quantity          NUMBER(20)      NOT NULL, 
  PRIMARY KEY (order_details_id), 
  CONSTRAINT FK_od_order_id FOREIGN KEY (order_id) REFERENCES orders_inv(order_id), 
  CONSTRAINT FK_od_product_id FOREIGN KEY (product_id) REFERENCES product_inv(product_id)  
); 
 
